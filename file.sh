	Types of Version Control System
	- Local Version Control
	Pros: Write and code changes are kept in a local computer
	Cons: Multiple people parallelly working on same project
	
	- Centralized Version Control
	Pros: Write and Commit all codes in one server
	Cons: When servers go down, no way to access the code
	
	- Distributed Version Control (we will use this!)
Pros: Write and Commit all codes on local systems and remote server
